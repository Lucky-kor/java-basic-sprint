package javabasic;

public class Example02 {
    public static void main(String[] args) {
        int leftNumber = 10;
        int rightNumber = 5;

        System.out.printf("%d와 %d의 합은 : %d입니다.%n", leftNumber, rightNumber, leftNumber + rightNumber);
        System.out.println(leftNumber - rightNumber);
        System.out.println(leftNumber * rightNumber);
        System.out.println(leftNumber / rightNumber);
    }
}
